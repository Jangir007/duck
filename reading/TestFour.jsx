import React, { useState } from "react";

function TestFour() {
    const [tasks, setTasks] = useState([]);
    const [newTask, setNewTask] = useState('');

    const addTask = () => {
        if (newTask.trim() !== "") {
            setTasks([...tasks, newTask]);
            setNewTask('');
        }
    };

    return (
        <div className="page">
            <h4 className="test-text">
                Қазіргі таңда технологиялар адам өмірінің ажырамас бөлігіне айналды. Жасанды интеллект, робототехника және жоғары жылдамдықты интернет біздің күнделікті тіршілігімізді түбегейлі өзгертуде. Технологиялар тек жұмысты жеңілдетіп қана қоймай, білім беру, медицина және ғылым салаларында жаңа мүмкіндіктер ашып жатыр. Болашаққа көз жүгіртсек, цифрландыру үдерісі одан әрі тереңдей түсетіні сөзсіз.

                Дегенмен, технологиялардың дамуы өз кезегінде жауапкершілікті де талап етеді. Деректер қауіпсіздігі, кибершабуылдар және адам мен машина арасындағы қарым-қатынас мәселелері бүгінгі таңның өзекті тақырыбына айналды. Бағдарламашылар мен инженерлер үшін басты міндет — адамзатқа пайдасын тигізетін әрі қауіпсіз жаңа өнімдер жасау. Технологияларды дұрыс бағытта қолдана білсек, біз әлдеқайда жарқын және дамыған болашаққа жетеріміз анық. Білім мен инновация — ертеңгі күннің негізгі кілті.
            </h4>
            <h2 className="test-title">Технологиялардың болашағы туралы өз көзқарасыңызды білдіріңіз.</h2>
            <ul className="test-ul">
                {tasks.map((task, index) => (
                    <li key={index}>{task}</li>
                ))}
            </ul>
            <input 
                onChange={(e) => setNewTask(e.target.value)} 
                value={newTask} 
                type="text" 
                placeholder="Введи предложение"
                className="test-input"
            />
            <button onClick={addTask} className="test-button">Отправить</button>
        </div>
    );
}

export default TestFour;